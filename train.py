"""
train.py

Fine-tunes Stable Video Diffusion (SVD) with LoRA adapters on the
pixel landslide training dataset generated by create_landslide_traindata.py.

Batch training workflow:
  1. Download 5 new images from Wikimedia Commons
  2. Generate training data (frames + videos)
  3. If first run: Create model from scratch
     Else: Load existing checkpoint and update with new data
  4. Train for 1 epoch on the new batch
  5. Clear training data directory
  6. Repeat until total_epochs == total_images_processed

Usage:
    accelerate launch train.py \\
        --data-dir data/training \\
        --images-dir data/raw-images \\
        --output-dir checkpoints \\
        --num-epochs 300 \\
        --batch-size 5
"""

