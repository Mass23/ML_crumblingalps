def main() -> None:
    parser = argparse.ArgumentParser(
        description="Fine-tune Stable Video Diffusion with LoRA on landslide training data"
    )

    # Data arguments
    parser.add_argument("--data-dir", type=str, required=True,
                        help="Directory with training data from create_landslide_traindata.py")
    parser.add_argument("--output-dir", type=str, default="checkpoints",
                        help="Directory for saving checkpoints (default: checkpoints)")
    parser.add_argument("--base-model", type=str,
                        default="stabilityai/stable-video-diffusion-img2vid",
                        help="HuggingFace model ID for the base SVD model")
    parser.add_argument("--resume-from", type=str, default=None,
                        help="Path to a checkpoint to resume training from")

    # Training hyperparameters
    parser.add_argument("--num-epochs", type=int, default=50,
                        help="Number of training epochs (default: 50)")
    parser.add_argument("--learning-rate", type=float, default=1e-4,
                        help="Learning rate (default: 1e-4)")
    parser.add_argument("--batch-size", type=int, default=1,
                        help="Per-device batch size (default: 1)")
    parser.add_argument("--gradient-accumulation-steps", type=int, default=4,
                        help="Gradient accumulation steps (default: 4)")
    parser.add_argument("--mixed-precision", type=str, default="fp16",
                        choices=["no", "fp16", "bf16"],
                        help="Mixed precision training (default: fp16)")
    parser.add_argument("--gradient-checkpointing", action="store_true",
                        help="Enable gradient checkpointing to save memory")
    parser.add_argument("--dataloader-num-workers", type=int, default=4,
                        help="Number of DataLoader workers (default: 4)")

    # LoRA arguments
    parser.add_argument("--lora-rank", type=int, default=32,
                        help="LoRA rank (default: 32)")

    # Video arguments
    parser.add_argument("--num-frames", type=int, default=14,
                        help="Number of video frames per sample (default: 14)")
    parser.add_argument("--image-size", type=int, default=512,
                        help="Image size in pixels (default: 512)")
    parser.add_argument("--fps", type=int, default=14,
                        help="Frames per second for conditioning (default: 14)")

    # Logging and checkpointing
    parser.add_argument("--save-every", type=int, default=5,
                        help="Save checkpoint every N epochs (default: 5)")
    parser.add_argument("--report-to", type=str, default="tensorboard",
                        choices=["tensorboard", "none"],
                        help="Where to log training metrics (default: tensorboard)")
    parser.add_argument("--seed", type=int, default=42,
                        help="Random seed for reproducibility (default: 42)")

    # Batch training arguments
    parser.add_argument("--batch-mode", action="store_true",
                        help="Enable batch-based training pipeline (generate -> train -> repeat)")
    parser.add_argument("--images-dir", type=str, default=None,
                        help="Directory with raw source images (required for batch-mode)")
    parser.add_argument("--batch-progress-file", type=str, default="batch_progress.json",
                        help="Path to JSON file tracking batch progress (default: batch_progress.json)")
    parser.add_argument("--batches-to-process", type=int, default=None,
                        help="Limit number of batches to process (default: None = all)")
    
    # Data generation arguments (for batch mode)
    parser.add_argument("--duration", type=int, default=4,
                        help="Video duration in seconds for generated data (default: 4)")
    parser.add_argument("--target-width", type=int, default=512,
                        help="Target image width in pixels for generated data (default: 512)")
    parser.add_argument("--target-height", type=int, default=512,
                        help="Target image height in pixels for generated data (default: 512)")

    args = parser.parse_args()

    # Route to appropriate training mode
    if args.batch_mode:
        if args.images_dir is None:
            raise ValueError("--images-dir is required when using --batch-mode")
        batch_training_pipeline(args)
    else:
        train(args)


if __name__ == "__main__":
    main()