def setup_lora(unet: UNetSpatioTemporalConditionModel, lora_rank: int = 32) -> UNetSpatioTemporalConditionModel:
    """
    Apply LoRA adapters to the UNet.

    Args:
        unet: The SVD UNet model
        lora_rank: LoRA rank (higher = more parameters, better quality)

    Returns:
        UNet with LoRA adapters attached
    """
    lora_config = LoraConfig(
        r=lora_rank,
        lora_alpha=lora_rank,
        target_modules=["to_k", "to_q", "to_v", "to_out.0"],
        lora_dropout=0.0,
        bias="none",
    )
    unet = get_peft_model(unet, lora_config)
    unet.print_trainable_parameters()
    return unet


