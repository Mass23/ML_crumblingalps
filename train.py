class LandslideDataset(Dataset):
    """
    PyTorch Dataset that loads (source_image, video_frames) pairs.

    Expects training data in the format produced by create_landslide_traindata.py:
        data_dir/
            source_{name}.png
            frames_{name}/
                frame_00000.png
                frame_00001.png
                ...
    """

    def __init__(
        self,
        data_dir: str,
        num_frames: int = 14,
        image_size: Tuple[int, int] = (512, 512),
    ) -> None:
        """
        Initialize the dataset.

        Args:
            data_dir: Root directory containing training data
            num_frames: Number of video frames to use per sample
            image_size: (height, width) for resizing images
        """
        self.data_dir = Path(data_dir)
        self.num_frames = num_frames
        self.image_size = image_size

        # Find all source images and pair them with frame directories
        self.samples = []
        for source_path in sorted(self.data_dir.glob("source_*.png")):
            name = source_path.stem.replace("source_", "", 1)
            frames_dir = self.data_dir / f"frames_{name}"
            if frames_dir.exists():
                frame_files = sorted(frames_dir.glob("frame_*.png"))
                if len(frame_files) >= num_frames:
                    self.samples.append((source_path, frame_files))

        logger.info(f"Found {len(self.samples)} training samples in '{data_dir}'")

        self.image_transform = transforms.Compose([
            transforms.Resize(image_size),
            transforms.ToTensor(),
            transforms.Normalize(mean=[0.5, 0.5, 0.5], std=[0.5, 0.5, 0.5]),
        ])

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> dict:
        """
        Load a single training sample.

        Returns:
            Dict with:
                - 'source_image': Tensor [3, H, W] (normalized)
                - 'frames': Tensor [T, 3, H, W] (normalized video frames)
        """
        source_path, frame_files = self.samples[idx]

        # Load source image
        source_img = Image.open(source_path).convert("RGB")
        source_tensor = self.image_transform(source_img)

        # Load evenly spaced frames from the sequence
        frame_indices = torch.linspace(0, len(frame_files) - 1, self.num_frames).long()
        frames = []
        for i in frame_indices:
            frame = Image.open(frame_files[i]).convert("RGB")
            frames.append(self.image_transform(frame))

        frames_tensor = torch.stack(frames)  # [T, 3, H, W]

        return {
            "source_image": source_tensor,
            "frames": frames_tensor,
        }


